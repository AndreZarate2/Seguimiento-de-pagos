drop policy if exists payment_receipts_admin_delete on storage.objects;
create policy payment_receipts_admin_delete
on storage.objects
for delete
to authenticated
using (
  bucket_id = 'payment-receipts'
  and lower(coalesce(auth.jwt() ->> 'email', '')) = 'andrezarate1234@gmail.com'
